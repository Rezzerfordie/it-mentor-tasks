from math import sqrt
a = input("Введите первое число: ")
b = input("Введите второе число: ")
a = float(a)
b = float(b)
summa = a + b
diff = a - b
mult = a * b
chast = sqrt(a)/sqrt(b)
print('Сумма двух чисел = ', summa)
print('Разность двух чисел = ', diff)
print('Произведние двух чисел = ', mult)
print('Частное квадратов двух чисел = ', chast)