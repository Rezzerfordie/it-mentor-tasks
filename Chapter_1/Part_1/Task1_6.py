a = input("Введите длину ребра a прямоугольного параллелепипеда: ")
b = input("Введите длину ребра b прямоугольного параллелепипеда: ")
c = input("Введите длину ребра c прямоугольного параллелепипеда: ")
V = float(a) * float(b) * float(c)
S = 2 * (float(a)*float(b) + float(b)*float(c) + float(a)*float(c))
print("Объем прямоугольного параллелепипеда =", V)
print('Площадь прямоугольного параллелепипеда =', S)
